package it.unifi.hierarchical.model;

/**
 * Represents the type of regions that can be found in the composite step. A composite step with FIRST regions will
 * move on to the next step as soon as one region ends, while a composite step with LAST regions will wait for all
 * regions to end before moving on the next step.
 */
public enum CompositeStepType {
    FIRST,
    LAST
}
